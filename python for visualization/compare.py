def compare_arrays(arr1, arr2):

  if len(arr1) != len(arr2):
    raise ValueError("The two arrays must have the same length")

  count = 0
  for i in range(len(arr1)):
    if arr1[i] == arr2[i]:
      count += 1

  return (count / len(arr1)) * 100

